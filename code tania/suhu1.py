def cel_fahrenheit (x):
    a = (9/5 * x) + 32
    print (f"Jadi Hasil dari c ke f adalah {a}")
    return a


def cel_reamur (x):
    a = (4/5*x)
    print(f"jadi hasil dari {x}C menjadi {a}R")
    return a


def cel_kelvin(x):
    a = x + 273
    print(f"Jadi hasil dari {x}C menjadi {a}K")
    return a


def feh_cel (x):
    a = 5/9 * (x-32)
    print(f"jadi hasil {x}F menjadi {a}C")
    return a
